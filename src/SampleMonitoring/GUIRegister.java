    public boolean verifyFields(){
        String uname = usernameTf.getText();
        String password = passwordTf.getText();
        String phone = phoneTf.getText();
        String email = emailTf.getText();
        
        if(uname.trim().equals("") || phone.trim().equals("") || email.trim().equals("")){
            JOptionPane.showMessageDialog(null, "One or more fields are empty!");
            return false;
        }else{
            return false;
        }
    }
    
    public boolean checkUsername(String username){
        PreparedStatement st;
        ResultSet rs;
        boolean username_exists = false;
        
        String query = "SELECT * FROM `users` WHERE `username` = ?";
        
        try {
            st = My_connect.getConnection().prepareStatement(query);
            st.setString(1, username);
            rs = st.executeQuery();
            
            if(rs.next()){
                username_exists = true;
                JOptionPane.showMessageDialog(null, "Username already in database");
            }
        } catch (SQLException ex) {
            Logger.getLogger(GUIRegister.class.getName()).log(Level.SEVERE, null, ex);
        }
        return username_exists;
        
                
    }
