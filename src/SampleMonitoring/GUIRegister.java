    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        // TODO add your handling code here:
        String username = usernameTf.getText();
        String password = passwordTf.getText();
        String phone = phoneTf.getText();
        String email = emailTf.getText();
        
        if(!checkUsername(username)){
            PreparedStatement ps;
            ResultSet rs;
            String registreUserQuery = "INSERT INTO `users`( `username`, `password`, `phone`, `email`) VALUES (?,?,?,?)";
            
            try {
                ps = My_connect.getConnection().prepareStatement(registreUserQuery);
                
                ps.setString(1, username);
                ps.setString(2, password);
                ps.setString(3, phone);
                ps.setString(4, email);
                
                if(ps.executeUpdate()!= 0){
                    JOptionPane.showMessageDialog(null, "Account created");
                    GUILogin l = new GUILogin();
                    l.setVisible(true);
                    l.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "Error creating");
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(GUIRegister.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        
        
        
    }//GEN-LAST:event_registerBtnActionPerformed

